// 2つの変数xとyの合計と平均を表示
class SumAve1 {
    public static void main(String[] args) {
        int x;  // xはint型の変数
        int y;  // yはint型の変数

        x = 63; // xに63を代入
        y = 18; // yに18を代入

        System.out.println("xの値は" + x + "です.");            //
        System.out.println("yの値は" + y + "です.");            //
        System.out.println("合計は" + (x + y) + "です.");       //
        System.out.println("平均は" + (x + y) / 2 + "です.");   //
    }
}
